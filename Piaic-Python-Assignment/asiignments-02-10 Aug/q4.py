# Prompt the user to enter the lengths of each side of a triangle
side1 = float(input("What is the length of side 1? "))  # Casting input to float
side2 = float(input("What is the length of side 2? "))  # Casting input to float
side3 = float(input("What is the length of side 3? "))  # Casting input to float

# Calculate the perimeter of the triangle
perimeter = side1 + side2 + side3

# Print the perimeter of the triangle
print(f"The perimeter of the triangle is {perimeter}")
